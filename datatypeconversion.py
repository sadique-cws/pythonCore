# in Python programming type conversion is the process of converting data from one data type to another. for
# example converting (int) data to (string)

# there are two type of conversion 
# implicit Conversion = automatic type conversion
# explicit Conversion = manual type conversion


# 1. implicit type conversion 
# in certain situation python automatically convert on data type to another. 

num = 2.4
intdata = 25

print(num + intdata)

# 2. explicit type conversion 
# in this type of conversion user convert the data type of an object required data type.

num = "25"
data = 24

print(int(num) + data)

